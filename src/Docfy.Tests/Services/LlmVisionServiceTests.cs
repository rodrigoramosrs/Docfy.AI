    private readonly ILlmVisionService _llmVisionService;
    private readonly Mock<ILogger<LlmVisionService>> _mockLogger;
    private readonly Mock<IHttpClientFactory> _mockHttpClientFactory;
    private readonly Mock<IConfiguration> _mockConfiguration;
    private readonly Mock<HttpClient> _mockHttpClient;

    public LlmVisionServiceTests()
    {
        _mockLogger = new Mock<ILogger<LlmVisionService>>();
        _mockHttpClientFactory = new Mock<IHttpClientFactory>();
        _mockConfiguration = new Mock<IConfiguration>();
        _mockHttpClient = new Mock<HttpClient>();

        _mockConfiguration.Setup(c => c["LlmVision:ApiKey"]).Returns("test-api-key");
        _mockConfiguration.Setup(c => c["LlmVision:Model"]).Returns("gpt-4-vision-preview");
        _mockConfiguration.Setup(c => c["LlmVision:MaxTokens"]).Returns("4096");
        _mockConfiguration.Setup(c => c["LlmVision:RequestTimeoutSeconds"]).Returns("30");
        _mockConfiguration.Setup(c => c["LlmVision:Endpoint"]).Returns("https://api.openai.com/v1/chat/completions");

        _mockHttpClientFactory.Setup(f => f.CreateClient("LlmClient")).Returns(_mockHttpClient.Object);

        _llmVisionService = new LlmVisionService(
            _mockHttpClientFactory.Object,
            _mockConfiguration.Object,
            _mockLogger.Object
        );
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldReturnImageAnalysisResult_WhenValidImageProvided()
    {
        var extractedImage = CreateMockExtractedImage("image/png");

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.Equal(extractedImage.PageNumber, result.PageNumber);
        Assert.Equal(extractedImage.ImageIndex, result.ImageIndex);
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldHandleLargeImages()
    {
        var extractedImage = CreateMockExtractedImage("image/jpeg");
        extractedImage.Data = CreateLargeImageData(3000);

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.Equal(extractedImage.PageNumber, result.PageNumber);
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldIdentifyDecorativeImages()
    {
        var extractedImage = CreateMockExtractedImage("image/png");

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.Equal(extractedImage.ImageIndex, result.ImageIndex);
        result.IsDecorative = true;
        result.ContentType = "Decorative";
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldIdentifyTextImages()
    {
        var extractedImage = CreateMockExtractedImage("image/png");
        extractedImage.Data = CreateTextImageData();

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.Equal(extractedImage.ImageIndex, result.ImageIndex);
        result.ContentType = "Text";
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldHandleMultipleFormatImages()
    {
        var formats = new[] { "image/jpeg", "image/png", "image/gif", "image/webp", "image/bmp" };

        foreach (var format in formats)
        {
            var extractedImage = CreateMockExtractedImage(format);

            var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

            Assert.NotNull(result);
            Assert.Equal(format, result.MimeType);
        }
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldReturnProcessedScale()
    {
        var extractedImage = CreateMockExtractedImage("image/png");

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.Equal(1.0, result.ProcessedScale);
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldCalculateImageHash()
    {
        var extractedImage = CreateMockExtractedImage("image/png");

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.NotNull(result.ImageHash);
        Assert.NotEmpty(result.ImageHash);
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldHandleEmptyImageData()
    {
        var extractedImage = CreateMockExtractedImage("image/png");
        extractedImage.Data = Array.Empty<byte>();

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.Equal(extractedImage.ImageIndex, result.ImageIndex);
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldHandleInvalidImageData()
    {
        var extractedImage = CreateMockExtractedImage("image/png");
        extractedImage.Data = new byte[] { 0x00, 0x01, 0x02, 0x03 };

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.Equal(extractedImage.ImageIndex, result.ImageIndex);
    }

    [Fact]
    public async Task AnalyzeImageAsync_ShouldIncludeImageBase64()
    {
        var extractedImage = CreateMockExtractedImage("image/png");

        var result = await _llmVisionService.AnalyzeImageAsync(extractedImage);

        Assert.NotNull(result);
        Assert.NotEmpty(result.ImageBase64);
        Assert.StartsWith("data:image/png;base64,", result.ImageBase64);
    }

